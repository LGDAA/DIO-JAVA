public class operadoresAritmeticos {
    public static void main(String[] args) throws Exception {
    // Operadores aritméticos
    double soma = 10.5 + 15.7;
    int subtracao = 113 - 25;
    int multiplicacao = 20 * 7;
    int divisao = 15 / 3;
    int modulo = 18 % 3;
    double resultado = (10 * 7) + (20/4);

    System.out.println("A soma entre 10.5 e 15.7 é: "+ soma);
    System.out.println("A subtração entre 113 e 25 é: "+ subtracao);
    System.out.println("A multpicação entre 20 e 7 é: "+ multiplicacao);
    System.out.println("A divisao entre 15 e 3 é: " + divisao);
    System.out.println("O resto da operação 18/3 é: " + modulo);
    System.out.println("O resultado da operação é: " + resultado);
    }
}
    
